bool MysqlDao::GetMaybeList(int self_id, std::vector<std::shared_ptr<MaybeInfo>>& _maybe_list)
{
	auto con = pool_->getConnection();
	if (con == nullptr) {
		return false;
	}

	Defer defer([this, &con]() {
		pool_->returnConnection(std::move(con));
		});


	try {
		// 第一步：获取用户的好友列表（考虑双向关系）
		std::set<int> friends;
		{
			std::unique_ptr<sql::PreparedStatement> pstmt(con->_con->prepareStatement(
				"SELECT friend_id FROM friend WHERE self_id = ? "
				"UNION SELECT self_id FROM friend WHERE friend_id = ?"));
			pstmt->setInt(1, self_id);
			pstmt->setInt(2, self_id);
			std::unique_ptr<sql::ResultSet> res(pstmt->executeQuery());
			while (res->next()) {
				friends.insert(res->getInt(1));
			}
		}

		// 第二步：批量获取二度好友及其度数
		std::map<int, double> maybe_friends; // key: user_id, value: score
		std::unique_ptr<sql::PreparedStatement> pstmt(con->_con->prepareStatement(
			"SELECT f2.friend_id, COUNT(f3.friend_id) AS degree "
			"FROM friend f1 "
			"JOIN friend f2 ON f1.friend_id = f2.self_id "
			"LEFT JOIN friend f3 ON f2.friend_id = f3.self_id "
			"WHERE f1.self_id = ? "
			"GROUP BY f2.friend_id"));
		pstmt->setInt(1, self_id);
		std::unique_ptr<sql::ResultSet> res(pstmt->executeQuery());

		while (res->next()) {
			int maybe_id = res->getInt("friend_id");
			int degree = res->getInt("degree");

			// 排除自己、已有好友及度数 = 0 的用户
			if (maybe_id == self_id || friends.count(maybe_id) || degree == 0) {
				std::cerr << "Excluded maybe_id: " << maybe_id
					<< " (self_id=" << (maybe_id == self_id)
					<< ", is_friend=" << friends.count(maybe_id)
					<< ", degree_zero=" << (degree == 0) << ")" << std::endl;
				continue;
			}

			// Adamic-Adar 评分
			double score = 1.0 / std::log(degree + 1);
			maybe_friends[maybe_id] += score;
			std::cerr << "Added maybe_id: " << maybe_id
				<< ", degree: " << degree << ", score: " << score << std::endl;
		}

		// 第三步：获取用户信息并构建 MaybeInfo
		for (const auto& pair : maybe_friends) {
			int maybe_id = pair.first;
			double score = pair.second;

			auto user_info = GetUser(maybe_id);
			if (user_info == nullptr) {
				std::cerr << "Failed to get user info for user_id: " << maybe_id << std::endl;
				continue;
			}

			auto maybe_info = std::make_shared<MaybeInfo>(
				maybe_id,
				user_info->name,
				user_info->desc,
				user_info->icon,
				user_info->nick,
				user_info->sex,
				score
			);
			_maybe_list.push_back(maybe_info);
		}

		// 第四步：按评分降序排序
		std::sort(_maybe_list.begin(), _maybe_list.end(),
			[](const auto& a, const auto& b) { return a->_score > b->_score; });

		return true;
	}
	catch (sql::SQLException& e) {
		std::cerr << "SQLException: " << e.what();
		std::cerr << " (MySQL error code: " << e.getErrorCode();
		std::cerr << ", SQLState: " << e.getSQLState() << " )" << std::endl;
		return false;
	}

	return true;
}
